    public void mostrarDatos(CuentaBancaria cuenta_ini) {
        CuentaEmpresa cuenta_mostrar = (CuentaEmpresa) cuenta_ini;
        txt_Nombre.setText(cuenta_mostrar.titular.nombre);
        txt_Apellidos.setText(cuenta_mostrar.titular.apellidos);
        txt_F_nac.setText(cuenta_mostrar.titular.fecha_nacimiento);
        txt_ccc.setText(cuenta_mostrar.ccc);
        txt_Saldo.setText(Double.toString (cuenta_mostrar.saldo));
        txt_Tipo.setText(Float.toString(cuenta_mostrar.tipo_descubierto));
        txt_Maximo.setText(Float.toString(cuenta_mostrar.maximo));
        
        //Creo un iterador para recorrer el HashMap.
        Iterator it = cuenta_mostrar.lista.keySet().iterator(); //el iterador obtiene las key del hashMap
        int contador_linea = 0;
        while(it.hasNext()) {
            String entidad = it.next().toString();
            Tb_entidad.setValueAt(entidad, contador_linea, 0);
            Tb_entidad.setValueAt(cuenta_mostrar.lista.get(entidad), contador_linea, 1);
            contador_linea++;
        }
         
        btn_ClienteAhorro.setEnabled(false);
    }

