    private void btn_ClienteAhorroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_ClienteAhorroMouseClicked
        // TODO add your handling code here:
        String nombre = txt_Nombre.getText();
        String apellidos = txt_Apellidos.getText();
        String fecha = txt_F_nac.getText();
        cliente = new Persona(nombre, apellidos, fecha);
        try {
            String ccc = txt_ccc.getText();
            boolean validar = AplicacionCuentaBancaria.ValidaCCC(ccc);
            if (validar) {
                JOptionPane.showMessageDialog(null, "Cuenta correcta", "Validación de Cuenta", JOptionPane.INFORMATION_MESSAGE);
                ccc = ccc.trim();
                ccc = ccc.replace(" ", "");
                double saldo = Double.parseDouble(txt_Saldo.getText());
                
                //DefaultTableModel modtabla = (DefaultTableModel) Tb_entidad.getModel(); //Obtenemos el modelo de la tabla, que es la que almacena los datos
                entidades = new HashMap<>();
                //IMPORTANTE: Funcionó después de cambiar en Design, en la JTable, el Type en String y Double
                
                try {
                    entidades.put(Tb_entidad.getValueAt(0,0).toString(), (double) Tb_entidad.getValueAt(0,1));    
                    entidades.put(Tb_entidad.getValueAt(1,0).toString(), (double) Tb_entidad.getValueAt(1,1));
                    entidades.put(Tb_entidad.getValueAt(2,0).toString(), (double) Tb_entidad.getValueAt(2,1));
                    entidades.put(Tb_entidad.getValueAt(3,0).toString(), (double) Tb_entidad.getValueAt(3,1));
                    entidades.put(Tb_entidad.getValueAt(4,0).toString(), (double) Tb_entidad.getValueAt(4,1));
                    entidades.put(Tb_entidad.getValueAt(5,0).toString(), (double) Tb_entidad.getValueAt(5,1));
                } catch (Exception j) {
                    //JOptionPane.showMessageDialog(null,"Error al leer la segunda línea", "Error", JOptionPane.ERROR_MESSAGE);
                }
                                              
                float tipo = Float.parseFloat(txt_Tipo.getText());
                float maximo = Float.parseFloat(txt_Maximo.getText());
                cuenta = new CuentaEmpresa(cliente, saldo, ccc, entidades, tipo, maximo);
                AplicacionCuentaBancaria.Cuentas.add(cuenta);
                JOptionPane.showMessageDialog(null,"Cuenta corriente creada", "Cuenta Corriente",JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "La cuenta no ha superado la validación", "Validación de Cuenta", JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error al crear la cuenta, por favor compruebe que los datos son correctos", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_ClienteAhorroMouseClicked


